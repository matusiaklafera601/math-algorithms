def calculate_sum(a, b):
    """
    Calculate the sum of two numbers.
    
    Parameters:
    a (int): The first number.
    b (int): The second number.
    
    Returns:
    int: The sum of a and b.
    """
    return a + b

# Example usage:
result = calculate_sum(2, 3)
print(result)  # Output: 5
