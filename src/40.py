def find_max_in_tuple(tup):
    """
    Find the maximum value in a tuple.
    
    Parameters:
    tup (tuple): A tuple of numbers.
    
    Returns:
    int: The maximum number in the tuple.
    """
    return max(tup)

# Example usage:
my_list = [10, 23, -5, 78, 14]
print(find_max_in_tuple(my_list))  # Output: 78
